package com.myparticularcollection;

public class ElementoException extends Exception {

	public ElementoException() {
		super();
		
	}

	public ElementoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ElementoException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ElementoException(String message) {
		super(message);
		
	}

	public ElementoException(Throwable cause) {
		super(cause);
		
	}
	
}
