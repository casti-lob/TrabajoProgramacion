package com.myparticularcollection;

public class SerieException extends Exception {

	public SerieException() {
		super();
		
	}

	public SerieException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public SerieException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public SerieException(String message) {
		super(message);

	}

	public SerieException(Throwable cause) {
		super(cause);
	
	}
	
}
